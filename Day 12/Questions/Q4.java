// Create a program that shows bitwise NOT of two numbers

public class Q4 {
    public static void main(String[] args) {
        int firstNumber = 20;
        int secondNumber = 25;

        int fristResult = ~firstNumber;
        int secondResult = ~secondNumber;

        System.out.println("Result of the bitwise NOT : " + fristResult);
        System.out.println("Result of the bitwise NOT : " + secondResult);
    }
}
