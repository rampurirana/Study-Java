// Loop : Loop is a control flow statement that allow code to be executed repeatedly based on a conditon

// 1. for loop : Used when the number of iteration is known
public class forLoop {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) { // i=0: initialized, i<5: conditon, i++: increasing
            System.out.println(i);
        }
    }
}